def add(a,b):
    print(a + b)
    return a + b

def sub(a,b):
    print(a - b)
    return a - b

def mul(a,b):
    print(a * b)
    return a * b

def div(a,b):
    print(a / b)
    return a / b

def sqr(a):
    print(a*a)
    return a*a

def sqrt(a):
    print(a**(1/2))
    return a**(1/2)

def exp(a,b):
    print(a**b)
    return a**b

